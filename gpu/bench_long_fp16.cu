#define REF_LEN 251

/* ================================================================
 * Run one generation test and compare
 * ================================================================ */

typedef struct {
    int gen_len;        /* how many tokens to generate */
    int match;          /* tokens matching reference */
    int first_div;      /* first divergence position (-1 if none) */
    int first_div_got;  /* GPU token at divergence */
    int first_div_ref;  /* reference token at divergence */
    float prefill_ms;
    float decode_ms;
} TestResult;

static TestResult run_test(LlamaGPU *ctx, int gen_len) {
    TestResult r;
    memset(&r, 0, sizeof(r));
    r.gen_len = gen_len;
    r.first_div = -1;

    int *output = (int*)malloc(gen_len * sizeof(int));

    cudaEvent_t t0, t1;
    CUDA_CHECK(cudaEventCreate(&t0));
    CUDA_CHECK(cudaEventCreate(&t1));

    llama_gpu_reset(ctx);

    /* Prefill */
    CUDA_CHECK(cudaEventRecord(t0, 0));
    int first_tok = 0;
    for (int p = 0; p < PROMPT_LEN; p++)
        first_tok = lgpu_forward_token(ctx, PROMPT[p], p);
    CUDA_CHECK(cudaEventRecord(t1, 0));
    CUDA_CHECK(cudaEventSynchronize(t1));
    CUDA_CHECK(cudaEventElapsedTime(&r.prefill_ms, t0, t1));

    output[0] = first_tok;

    /* Decode (respects MAX_SEQ boundary) */
    int actual_gen = 1;  /* output[0] already set */
    CUDA_CHECK(cudaEventRecord(t0, 0));
    for (int g = 1; g < gen_len; g++) {
        int pos = PROMPT_LEN + g - 1;
        if (pos >= LGPU_MAX_SEQ) break;
        output[g] = lgpu_forward_token(ctx, output[g-1], pos);
        actual_gen = g + 1;
    }
    CUDA_CHECK(cudaEventRecord(t1, 0));
    CUDA_CHECK(cudaEventSynchronize(t1));
    CUDA_CHECK(cudaEventElapsedTime(&r.decode_ms, t0, t1));
    r.gen_len = actual_gen;  /* may be less than requested */

    /* Compare against reference (use actual_gen, not requested gen_len,
     * to avoid reading uninitialized output if truncated by MAX_SEQ) */
    int cmp_len = actual_gen < REF_LEN ? actual_gen : REF_LEN;
    r.match = 0;
    for (int g = 0; g < cmp_len; g++) {
        if (output[g] == REF_251[g]) {
            r.match++;
        } else if (r.first_div < 0) {
            r.first_div = g;
            r.first_div_got = output[g];
            r.first_div_ref = REF_251[g];
        }
    }

    cudaEventDestroy(t0);
    cudaEventDestroy(t1);
    free(output);
    return r;
}

/* ================================================================
 * Main
 * ================================================================ */

int main(int argc, char **argv) {
    if (argc < 2) {
        fprintf(stderr, "Usage: %s <model_dir>\n", argv[0]);
        return 1;
    }

    cudaDeviceProp prop;
    CUDA_CHECK(cudaGetDeviceProperties(&prop, 0));
    printf("GPU: %s (%d SMs, compute %d.%d)\n",
           prop.name, prop.multiProcessorCount, prop.major, prop.minor);

    LlamaGPU ctx;
    printf("Loading model...\n");
    if (llama_gpu_init(&ctx, argv[1]) != 0) return 1;
    printf("Model loaded.\n");

    printf("\n=== Long-Generation Stress Test ===\n");
    printf("Prompt: france_capital (%d tokens)\n", PROMPT_LEN);
    printf("Reference: %d tokens from CPU Q16.48 oracle (greedy)\n", REF_LEN);
    printf("MAX_SEQ: %d (valid positions 0..%d)\n", LGPU_MAX_SEQ, LGPU_MAX_SEQ - 1);
    printf("Max gen for this prompt: %d (positions %d..%d)\n\n",
           LGPU_MAX_SEQ - PROMPT_LEN, PROMPT_LEN, LGPU_MAX_SEQ - 1);

    /* Test lengths: 20, 100, 200, 251 (true limit for 6-token prompt).
     * 251 is the exact max: decode g=250 feeds at pos=255 (last valid),
     * g=251 would hit pos=256 >= MAX_SEQ and break. */
    int test_lens[] = {20, 100, 200, 251};
    int n_tests = 4;

    printf("%-8s | %6s %6s | %9s %10s %10s | %s\n",
           "GenLen", "Match", "Total", "Prefill", "Decode", "tok/s", "Status");
    printf("%-8s | %6s %6s | %9s %10s %10s | %s\n",
           "--------", "------", "------", "---------", "----------", "----------", "--------");

    int all_pass = 1;

    for (int t = 0; t < n_tests; t++) {
        int gl = test_lens[t];

        TestResult r = run_test(&ctx, gl);
        /* r.gen_len may be < gl if truncated by MAX_SEQ */
        int decode_steps = r.gen_len - 1;
        float dpt = decode_steps > 0 ? r.decode_ms / decode_steps : 0;
        float tps = dpt > 0 ? 1000.0f / dpt : 0;

        const char *status;
        if (r.first_div < 0) {
            status = "PERFECT";
        } else {
            status = "DIVERGED";
            all_pass = 0;
        }

        int total_seq = PROMPT_LEN + r.gen_len;
        int cmp_len = r.gen_len < REF_LEN ? r.gen_len : REF_LEN;
        printf("%-8d | %3d/%3d %5d | %8.1f ms %8.1f ms %8.0f | %s",
               r.gen_len, r.match, cmp_len, total_seq,
               r.prefill_ms, r.decode_ms, tps, status);

        if (r.first_div >= 0)
            printf(" at pos %d (got %d, ref %d)", r.first_div, r.first_div_got, r.first_div_ref);
        printf("\n");
    }

    /* Summary */
    printf("\n========================================\n");
    if (all_pass) {
        printf("=== ALL TESTS PASSED — FP16 matches CPU oracle up to %d generated tokens ===\n",
               test_lens[n_tests - 1]);
        printf("=== Last fed position: %d (MAX_SEQ boundary at %d) ===\n",
               LGPU_MAX_SEQ - 1, LGPU_MAX_SEQ);
    } else {
        printf("=== DIVERGENCE DETECTED — see first_div positions above ===\n");
    }
    printf("========================================\n");

    llama_gpu_free(&ctx);
    printf("\nDone.\n");
    return 0;
}
