/*
 * bench_long_fp16.cu — Long-generation stress test for FP16 GPU runtime
 *
 * Validates GPU FP16 token accuracy against CPU Q16.48 oracle over
 * materially longer generation (100/200/244 tokens) and near MAX_SEQ.
 * Reports first divergence position, throughput vs context length.
 *
 * Build:
 *   NVCC=/usr/local/cuda-13.0/bin/nvcc
 *   $NVCC -O3 -arch=sm_120 bench_long_fp16.cu -o bench_long_fp16 -lcublas -lm
 *
 * Run:
 *   ./bench_long_fp16 /path/to/TinyLlama-model-dir
 *
 * Copyright (c) 2026 — MicroGPT-C R11 GPU stress validation
 */

#include "llama_gpu.cuh"

/* ================================================================
 * CPU oracle reference: france_capital, 251 generated tokens
 * Generated by: ./llama_int $MGW --native --ref-dir $HF --generate --max-new-tokens 251
 * Prompt: PROMPT_FRANCE = {1, 450, 7483, 310, 3444, 338} (6 tokens)
 * Last decode feeds at position 255 (guard fires at pos >= 256)
 * ================================================================ */

static const int PROMPT[] = {1, 450, 7483, 310, 3444, 338};
